const storageKeys = {
    language: "dxb-language",
    theme: "dxb-theme"
};

function getStoredValue(key) {
    try {
        return localStorage.getItem(key);
    } catch (error) {
        return null;
    }
}

function setStoredValue(key, value) {
    try {
        localStorage.setItem(key, value);
    } catch (error) {
        return false;
    }

    return true;
}

const html = document.documentElement;
const navbar = document.querySelector(".navbar");
const navLinks = document.querySelector(".nav-links");
const menuButton = document.querySelector(".menu-btn");
const menuIcon = menuButton?.querySelector("i");
const languageButtons = document.querySelectorAll(".lang-btn");
const languageSwitch = document.querySelector(".language-switch");
const themeToggle = document.querySelector("#theme-toggle");
const themeMeta = document.querySelector('meta[name="theme-color"]');
const contactForm = document.querySelector("#contact-form");
const formStatus = document.querySelector("#form-status");

let currentLanguage = getStoredValue(storageKeys.language) || "en";

if (!translations[currentLanguage]) {
    currentLanguage = "en";
}

function getPreferredTheme() {
    const savedTheme = getStoredValue(storageKeys.theme);

    if (savedTheme === "light" || savedTheme === "dark") {
        return savedTheme;
    }

    return "dark";
}

function setTheme(theme) {
    const selectedTheme = theme === "light" ? "light" : "dark";
    const isDark = selectedTheme === "dark";
    const copy = translations[currentLanguage];

    html.dataset.theme = selectedTheme;
    setStoredValue(storageKeys.theme, selectedTheme);

    if (themeToggle) {
        themeToggle.innerHTML = `<i class="fa-solid ${isDark ? "fa-sun" : "fa-moon"}"></i>`;
        themeToggle.setAttribute("aria-label", isDark ? copy.themeDark : copy.themeLight);
        themeToggle.setAttribute("title", isDark ? copy.themeDark : copy.themeLight);
    }

    if (themeMeta) {
        themeMeta.setAttribute("content", isDark ? "#08111f" : "#f4f8fb");
    }
}

function setLanguage(lang) {
    const selectedLanguage = translations[lang] ? lang : "en";
    const copy = translations[selectedLanguage];

    currentLanguage = selectedLanguage;
    html.lang = selectedLanguage;
    html.dir = selectedLanguage === "ar" ? "rtl" : "ltr";
    document.body.classList.toggle("ar-mode", selectedLanguage === "ar");
    setStoredValue(storageKeys.language, selectedLanguage);

    document.querySelectorAll("[data-i18n]").forEach((element) => {
        const key = element.dataset.i18n;

        if (copy[key]) {
            element.textContent = copy[key];
        }
    });

    document.querySelectorAll("[data-i18n-placeholder]").forEach((element) => {
        const key = element.dataset.i18nPlaceholder;

        if (copy[key]) {
            element.setAttribute("placeholder", copy[key]);
        }
    });

    languageButtons.forEach((button) => {
        const isActive = button.dataset.lang === selectedLanguage;
        button.classList.toggle("active", isActive);
        button.setAttribute("aria-pressed", String(isActive));
    });

    setTheme(html.dataset.theme || getPreferredTheme());
}

function closeMobileMenu() {
    navLinks?.classList.remove("active");
    languageSwitch?.classList.remove("open");
    menuButton?.setAttribute("aria-expanded", "false");
    menuButton?.setAttribute("aria-label", translations[currentLanguage].menuOpen);
    menuIcon?.classList.add("fa-bars");
    menuIcon?.classList.remove("fa-xmark");
}

function toggleMobileMenu() {
    const isOpen = navLinks.classList.toggle("active");

    languageSwitch?.classList.toggle("open", isOpen);
    menuButton.setAttribute("aria-expanded", String(isOpen));
    menuButton.setAttribute("aria-label", isOpen ? translations[currentLanguage].menuClose : translations[currentLanguage].menuOpen);
    menuIcon.classList.toggle("fa-bars", !isOpen);
    menuIcon.classList.toggle("fa-xmark", isOpen);
}

function updateNavbar() {
    navbar?.classList.toggle("sticky", window.scrollY > 24);
}

function showFormStatus(message, type = "info") {
    if (!formStatus) {
        return;
    }

    formStatus.textContent = message;
    formStatus.classList.toggle("error", type === "error");
}

function buildMailToUrl({ name, email, message }) {
    const copy = translations[currentLanguage];
    const subject = encodeURIComponent(`${copy.mailSubject} ${name}`);
    const body = encodeURIComponent(`Name: ${name}\nEmail: ${email}\n\nMessage:\n${message}`);

    return `mailto:${companyEmail}?subject=${subject}&body=${body}`;
}

function handleContactSubmit(event) {
    event.preventDefault();

    const nameInput = document.querySelector("#name-input");
    const emailInput = document.querySelector("#email-input");
    const messageInput = document.querySelector("#message-input");
    const name = nameInput?.value.trim();
    const email = emailInput?.value.trim();
    const message = messageInput?.value.trim();
    const copy = translations[currentLanguage];

    if (!name || !email || !message) {
        showFormStatus(copy.formRequired, "error");
        return;
    }

    if (!emailInput.checkValidity()) {
        showFormStatus(copy.formInvalidEmail, "error");
        emailInput.focus();
        return;
    }

    showFormStatus(copy.mailOpening);
    window.location.href = buildMailToUrl({ name, email, message });
}

window.setLanguage = setLanguage;

window.addEventListener("scroll", updateNavbar);

menuButton?.addEventListener("click", toggleMobileMenu);

navLinks?.querySelectorAll("a").forEach((link) => {
    link.addEventListener("click", closeMobileMenu);
});

languageButtons.forEach((button) => {
    button.addEventListener("click", () => {
        setLanguage(button.dataset.lang);
        closeMobileMenu();
    });
});

themeToggle?.addEventListener("click", () => {
    setTheme(html.dataset.theme === "dark" ? "light" : "dark");
});

contactForm?.addEventListener("submit", handleContactSubmit);

document.addEventListener("keydown", (event) => {
    if (event.key === "Escape") {
        closeMobileMenu();
    }
});

setTheme(getPreferredTheme());
setLanguage(currentLanguage);
updateNavbar();
